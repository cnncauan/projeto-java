package aulas;

public class Aula_40 {

    public static void main(String[] args) {
        int[] abc = {1,2,3,4,5};
        int x = 10;

        vetor(abc);
        variavePrimitiva(x);
    }
    public static void vetor(int[] x) {
        System.out.println(x[0]);
    }
    public static void variavePrimitiva(int x) {
        System.out.println(x);
    }

}
